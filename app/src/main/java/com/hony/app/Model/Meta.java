package com.hony.app.Model;

public class Meta{
    private String msg;
    private Number status;

    public Meta() {
    }

    public String getMsg() {
        return this.msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Number getStatus() {
        return this.status;
    }

    public void setStatus(Number status) {
        this.status = status;
    }
}